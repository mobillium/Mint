//                swift += "\n\n"
//            }
//            if index < keyValues.count - 1 {
//                swift += "\n"
//            }
//        }
        for key in keyValues {
            for mappedArr in key.value {
                swift += generateStructAndKeys(structName: keyValues.keys.first ?? "nil", keyValues: key.value)
                swift += "\n\n"
            }
        }
        swift += "}\n"
        return swift
    }
    
    private static func generateStructAndKeys(structName: String, keyValues: [String: String]) -> String {
        var swift = "    struct \(structName) {\n"
        for key in keyValues {
            let lowercasedKey = key.key.lowercaseFirstCharacter()
            let nsLocalizedKey = "\(structName).\(key.key)"
            swift += "\n        /// \(key.value)"
            swift += "\n        static let \(lowercasedKey) = NSLocalizedString(\"\(nsLocalizedKey)\", bundle: Bundle.main, comment: \"\")\n"
        }
        swift += "\n    }"
        return swift
    }
}
